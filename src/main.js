/*
 * #6
 *
 * Виконайте перейменування підрядка у рядку.
 * Як вихідний рядок використовуйте значення поля str об'єкта strObj (див.п.4), результат збережіть у змінній strReplace та відобразіть у консолі.
 * Вихідний рядок: 'Мама мыла раму, рама мыла маму'
 *      Результат: 'Мама моет раму, Рама держит маму'
 */

// strReplace
var strReplace = strObj.str
  .replace("мыла", "моет")    // заміна першого входження 'мыла' на 'моет'
  .replace("рама", "Рама")    // заміна першого входження 'рама' на 'Рама'
  .replace("мыла", "держит"); // заміна другого входження 'мыла' на 'держит'
console.log("Результат заміни: " + strReplace);

